    @Override
    public String run() {
        long count = 0;

        for (int i = this.getStartIndex(); i <= this.getEndIndex(); i++) {
            if (this.isTriangleWord(this.getWords().get(i))) {
                count++;
            }
        }

        return String.valueOf(count);
    }

    private boolean isTriangleWord(String word) {
        long wordSum = 0;
        for (char c : word.toCharArray()) {
            wordSum += c - 'A' + 1;
        }
        return Common.isTriangle(wordSum);
    }

